    public function run(): void
    {
        foreach ($this->categories as $categoryData) {
            $category = Category::updateOrCreate(
                ['code' => $categoryData['code']],
                [
                    'name' => $categoryData['name'],
                    'weight_percent' => $categoryData['weight'],
                    'order' => $categoryData['order'],
                ]
            );

            // Создаём индикаторы для категории
            if (isset($this->indicators[$categoryData['code']])) {
                foreach ($this->indicators[$categoryData['code']] as $indicatorData) {
                    Indicator::updateOrCreate(
                        [
                            'category_id' => $category->id,
                            'code_in_category' => $indicatorData['code'],
                        ],
                        [
                            'question_text' => $indicatorData['question'],
                            'unit' => $indicatorData['unit'],
                            'input_type' => $indicatorData['input_type'],
                            'filename_slug' => $indicatorData['slug'],
                            'description_help' => $indicatorData['help'],
                            'validation_rules' => $indicatorData['validation'],
                            'order' => $indicatorData['code'],
                        ]
                    );
                }
            }
        }

        $this->command->info(' GreenMetric индикаторы успешно загружены!');
        $this->command->info('   Категорий: ' . Category::count());
        $this->command->info('   Индикаторов: ' . Indicator::count());
    }
}
